# ISCO-08 1位大类均值（用于无法精确匹配时兜底）
# 数据来源：ILO Table A1 中各大类的加权平均估算
ISCO1_FALLBACK = {
    1: ('Minimal Exposure', 0.38),   # Managers
    2: ('Minimal Exposure', 0.38),   # Professionals
    3: ('Gradient 2', 0.43),         # Technicians and associate professionals
    4: ('Gradient 3', 0.55),         # Clerical support workers (highest avg)
    5: ('Gradient 1', 0.32),         # Service and sales workers
    6: ('Not Exposed', 0.22),        # Skilled agricultural
    7: ('Not Exposed', 0.21),        # Craft and related trades
    8: ('Not Exposed', 0.23),        # Plant and machine operators
    9: ('Not Exposed', 0.21),        # Elementary occupations
}

GRADIENT_TO_INT = {
    'Gradient 4': 4, 'Gradient 3': 3, 'Gradient 2': 2, 'Gradient 1': 1,
    'Minimal Exposure': 0, 'Not Exposed': -1,
}


def load_ilo_lookup():
    """返回 {isco4: (gradient, mean, sd)} 字典。"""
    lookup = {}
    with ILO_CSV.open(encoding='utf-8') as f:
        for row in csv.DictReader(f):
            code = int(row['isco08_4digit'])
            lookup[code] = (
                row['exposure_gradient'],
                float(row['mean_score']),
                float(row['sd_score']),
            )
    return lookup


def build_keyword_index():
    """构建 (pattern, isco4) 列表，已按规则顺序排列。"""
    rules = []
    for kw, isco4 in KEYWORD_RULES:
        rules.append((kw.lower(), isco4))
    return rules


def match_jobtitle(title: str, keyword_rules: list, ilo_lookup: dict):
    """
    返回 (isco4, gradient, mean_score, sd_score, match_method)
    match_method: 'keyword' | 'isco1_fallback' | 'unknown'
    """
    t = title.strip().lower()
    # 先精确子串匹配
    for kw, isco4 in keyword_rules:
        if kw in t:
            if isco4 in ilo_lookup:
                grad, mean, sd = ilo_lookup[isco4]
                return isco4, grad, mean, sd, 'keyword'
            else:
                # isco4 不在 ILO 表中 → 用 1位组后备
                isco1 = isco4 // 1000
                grad, mean = ISCO1_FALLBACK.get(isco1, ('Not Exposed', 0.20))
                return isco4, grad, mean, 0.10, 'keyword_noilo'
    # 无匹配 → 1位组后备（无法确定 isco4）
    return None, 'unknown', 0.25, 0.10, 'unknown'


def main():
    ilo_lookup = load_ilo_lookup()
    keyword_rules = build_keyword_index()

    print(f'ILO lookup: {len(ilo_lookup)} ISCO-08 4-digit occupations')

    # ── 读取 master，写出 master_with_ai_exposure ──
    match_stats = {'keyword': 0, 'keyword_noilo': 0, 'unknown': 0}
    yearly_data = defaultdict(list)  # year → [entropy, mean_score, gradient_int]

    OUT_MASTER.parent.mkdir(parents=True, exist_ok=True)

    with MASTER_CSV.open(encoding='utf-8-sig') as fin, \
         OUT_MASTER.open('w', encoding='utf-8-sig', newline='') as fout:

        reader = csv.DictReader(fin)
        extra_fields = ['isco08_4digit', 'ai_exposure_gradient',
                        'ai_mean_score', 'ai_sd_score', 'isco_match_method']
        writer = csv.DictWriter(fout, fieldnames=reader.fieldnames + extra_fields,
                                extrasaction='ignore')
        writer.writeheader()

        for row in reader:
            title = row.get('招聘岗位', '')
            isco4, grad, mean, sd, method = match_jobtitle(
                title, keyword_rules, ilo_lookup)

            match_stats[method] = match_stats.get(method, 0) + 1
            row['isco08_4digit'] = isco4 if isco4 else ''
            row['ai_exposure_gradient'] = grad
            row['ai_mean_score'] = f'{mean:.3f}'
            row['ai_sd_score'] = f'{sd:.3f}'
            row['isco_match_method'] = method
            writer.writerow(row)

            # 收集年度数据
            try:
                year = int(float(row.get('year', 0)))
                entropy = float(row.get('entropy_score', 0))
                if 2014 <= year <= 2025 and entropy > 0:
                    yearly_data[year].append((entropy, mean, GRADIENT_TO_INT.get(grad, -1)))
            except (ValueError, TypeError):
                pass

    total = sum(match_stats.values())
    print('\n匹配统计:')
    for k, v in sorted(match_stats.items(), key=lambda x: -x[1]):
        print(f'  {k:20s}: {v:>8,}  ({v/total*100:.1f}%)')

    # ── 汇总年度 AI exposure × entropy 数据 ──
    with OUT_YEARLY.open('w', encoding='utf-8-sig', newline='') as f:
        writer = csv.writer(f)
        writer.writerow(['year', 'gradient_group', 'n', 'mean_entropy', 'mean_ai_score'])

        for year in sorted(yearly_data.keys()):
            rows = yearly_data[year]
            # 按梯度分组
            by_grad = defaultdict(list)
            for entropy, ai_score, grad_int in rows:
                label = {4: 'Gradient4', 3: 'Gradient3', 2: 'Gradient2',
                         1: 'Gradient1', 0: 'Minimal', -1: 'NotExposed'}.get(grad_int, 'Unknown')
                by_grad[label].append((entropy, ai_score))

            for label, items in sorted(by_grad.items()):
                n = len(items)
                mean_e = sum(x[0] for x in items) / n
                mean_ai = sum(x[1] for x in items) / n
                writer.writerow([year, label, n, f'{mean_e:.6f}', f'{mean_ai:.3f}'])

    print(f'\n输出:')
    print(f'  {OUT_MASTER}')
    print(f'  {OUT_YEARLY}')


if __name__ == '__main__':
    main()
