# 编译正则
_RULES = []
for code, name, keywords in INDUSTRY_RULES:
    pattern = re.compile("|".join(re.escape(kw) for kw in keywords))
    _RULES.append((code, name, pattern))


_PAREN_CHARS = re.compile(r'[（()）]')

def classify(company_name):
    """返回 (行业代码, 行业名称) 或 None"""
    for code, name, pattern in _RULES:
        if pattern.search(company_name):
            return code, name
    # 去括号符号后再试（"金地（集团）" → "金地集团"）
    cleaned = _PAREN_CHARS.sub('', company_name)
    if cleaned != company_name:
        for code, name, pattern in _RULES:
            if pattern.search(cleaned):
                return code, name
    return None, None


def main():
    t0 = time.time()
    print("[1/2] Classifying companies...")

    industry_counter = Counter()
    total = 0
    matched = 0

    with open(MERGED_CSV, 'r', encoding='utf-8-sig') as fin, \
         open(OUTPUT_CSV, 'w', encoding='utf-8-sig', newline='') as fout:
        reader = csv.DictReader(fin)
        writer = csv.writer(fout)
        writer.writerow(["row_idx", "industry_code", "industry_name"])

        for i, row in enumerate(reader):
            total += 1
            name = row.get('企业名称', '').strip()
            code, ind_name = classify(name)

            if code:
                matched += 1
                industry_counter[f"{code} {ind_name}"] += 1
            writer.writerow([i, code or "", ind_name or ""])

            if (i + 1) % 5_000_000 == 0:
                print(f"  {i+1:,} rows, matched {matched:,} ({matched/(i+1)*100:.1f}%)", flush=True)

    elapsed = time.time() - t0
    print(f"\n[2/2] Done! {elapsed:.0f}s")
    print(f"  Total: {total:,}")
    print(f"  Matched: {matched:,} ({matched/total*100:.1f}%)")
    print(f"  Unmatched: {total-matched:,} ({(total-matched)/total*100:.1f}%)")
    print(f"\n  Industry distribution:")
    for k, v in industry_counter.most_common():
        print(f"    {k}: {v:,} ({v/total*100:.1f}%)")
    print(f"\n  Output: {OUTPUT_CSV}")


if __name__ == "__main__":
    main()
